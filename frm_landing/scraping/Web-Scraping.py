    "def normalizar_valores_monetarios(dataframe):\n",
    "    \"\"\"Normalización de valores monetarios - versión optimizada\"\"\"\n",
    "    for columna in dataframe.columns:\n",
    "        if any(keyword in columna.lower() for keyword in ['valor', 'precio', 'mercado', '€', 'eur', 'ppp']):\n",
    "            try:\n",
    "                def convertir_valor_monetario(valor):\n",
    "                    if pd.isna(valor) or valor in ['', 'None', 'nan', 'No disponible']:\n",
    "                        return None\n",
    "                    \n",
    "                    valor_str = str(valor).strip().lower()\n",
    "                    \n",
    "                    if 'mill' in valor_str:\n",
    "                        numero_match = re.search(r'([\\d,]+)', valor_str)\n",
    "                        if numero_match:\n",
    "                            numero_limpio = numero_match.group(1).replace(',', '.')\n",
    "                            try:\n",
    "                                return float(numero_limpio) * 1000000\n",
    "                            except:\n",
    "                                return valor\n",
    "                    \n",
    "                    elif 'mil' in valor_str:\n",
    "                        numero_match = re.search(r'([\\d,]+)', valor_str)\n",
    "                        if numero_match:\n",
    "                            numero_limpio = numero_match.group(1).replace(',', '.')\n",
    "                            try:\n",
    "                                return float(numero_limpio) * 1000\n",
    "                            except:\n",
    "                                return valor\n",
    "                    \n",
    "                    elif ',' in valor_str and re.match(r'^\\d+,\\d+$', valor_str):\n",
    "                        try:\n",
    "                            return float(valor_str.replace(',', '.'))\n",
    "                        except:\n",
    "                            return valor\n",
    "                    \n",
    "                    elif '.' in valor_str and ',' in valor_str:\n",
    "                        try:\n",
    "                            limpio = valor_str.replace('.', '').replace(',', '.')\n",
    "                            return float(limpio)\n",
    "                        except:\n",
    "                            return valor\n",
    "                    \n",
    "                    try:\n",
    "                        return float(valor_str)\n",
    "                    except:\n",
    "                        return valor\n",
    "                \n",
    "                nuevos_valores = dataframe[columna].apply(convertir_valor_monetario)\n",
    "                cambios = sum(dataframe[columna] != nuevos_valores)\n",
    "                if cambios > 0:\n",
    "                    dataframe[columna] = nuevos_valores\n",
    "                \n",
    "            except Exception:\n",
    "                pass\n",
    "    \n",
    "    return dataframe\n",
    "\n",
