    "def _convertir_formato_fecha_DD_MM_YYYY(fecha):\n",
    "    \"\"\"Convierte fechas textuales a formato estándar DD-MM-YYYY\"\"\"\n",
    "    meses = {\n",
    "        'enero': '01', 'febrero': '02', 'marzo': '03', 'abril': '04',\n",
    "        'mayo': '05', 'junio': '06', 'julio': '07', 'agusto': '08',\n",
    "        'septiembre': '09', 'octubre': '10', 'noviembre': '11', 'diciembre': '12'\n",
    "    }\n",
    "\n",
    "    if isinstance(fecha, str):\n",
    "        try:\n",
    "            fecha_sin_tilde = unidecode(fecha.strip().lower())\n",
    "            partes = fecha_sin_tilde.replace(',', '').split()\n",
    "\n",
    "            if len(partes) >= 6:\n",
    "                dia = partes[1]\n",
    "                mes = meses.get(partes[3], '01')\n",
    "                año = partes[5]\n",
    "                return f\"{dia.zfill(2)}-{mes}-{año}\"\n",
    "            elif len(partes) >= 4:\n",
    "                dia = partes[1]\n",
    "                mes = meses.get(partes[3], '01')\n",
    "                año = str(_AÑO_ACTUAL)  # ¡CORREGIDO! Ya no es 2025 hardcodeado\n",
    "                return f\"{dia.zfill(2)}-{mes}-{año}\"\n",
