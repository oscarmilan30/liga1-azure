    "                ayer = datetime.now() - timedelta(days=1)\n",
    "                return ayer.strftime(\"%d-%m-%Y\")\n",
    "            elif fecha_sin_tilde == 'hoy':\n",
    "                hoy = datetime.now()\n",
    "                return hoy.strftime(\"%d-%m-%Y\")\n",
    "            elif fecha_sin_tilde == 'manana':\n",
    "                manana = datetime.now() + timedelta(days=1)\n",
    "                return manana.strftime(\"%d-%m-%Y\")\n",
    "\n",
    "        except Exception:\n",
    "            return fecha\n",
    "    return fecha\n",
